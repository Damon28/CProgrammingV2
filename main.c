#include <stdio.h>
#include <stdlib.h>

/*https://www.youtube.com/watch?v=KJgsSFOSQv0&t*/

int main()
{
    printf("===While Loops===\n\n");

    int index = 1;
    while(index <= 5){
        printf("%d\n", index);
        index++; /*this is equivalent to 'index = index + 1;'*/
    }


    return 0;
}
